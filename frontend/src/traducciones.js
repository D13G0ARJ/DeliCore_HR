function traducirExacto(valor, idioma) {
  if (!valor || idioma === 'es') {
    return valor
  }

  const texto = String(valor).trim()
  const coincidenciaAntiguedad = texto.match(/^(\d+)\s+(año|años)$/i)

  if (coincidenciaAntiguedad) {
    const cantidad = coincidenciaAntiguedad[1]
    const unidad = cantidad === '1' ? 'year' : 'years'
    return `${cantidad} ${unidad}`
  }

  const normalizado = normalizarTexto(texto)

  const coincidencia = Object.entries(diccionarioDinamicoEn).find(
    ([llave]) => normalizarTexto(llave) === normalizado,
  )

  return coincidencia?.[1] ?? valor
}

function traducirArreglo(arreglo = [], idioma) {
  return arreglo.map((valor) =>
    typeof valor === 'string' ? traducirExacto(valor, idioma) : traducirEstructura(valor, idioma),
  )
}

function traducirObjeto(objeto = {}, idioma) {
  const resultado = {}

  Object.entries(objeto).forEach(([clave, valor]) => {
    if (Array.isArray(valor)) {
      resultado[clave] = traducirArreglo(valor, idioma)
      return
    }

    if (valor && typeof valor === 'object') {
      resultado[clave] = traducirObjeto(valor, idioma)
      return
    }

    resultado[clave] = typeof valor === 'string' ? traducirExacto(valor, idioma) : valor
  })

  return resultado
}

export function traducirEstructura(valor, idioma) {
  if (Array.isArray(valor)) {
    return traducirArreglo(valor, idioma)
  }

  if (valor && typeof valor === 'object') {
    return traducirObjeto(valor, idioma)
  }

  return typeof valor === 'string' ? traducirExacto(valor, idioma) : valor
}

export function localizarPanelGeneral(panel, idioma) {
  if (!panel) {
    return panel
  }

  const panelTraducido = traducirObjeto(panel, idioma)

  panelTraducido.resumen = (panelTraducido.resumen ?? []).map((item) => {
    if (idioma === 'es') {
      return item
    }

    const tendenciaNormalizada = normalizarTexto(item.tendencia)

    if (tendenciaNormalizada.includes('AREAS PILOTO LISTAS')) {
      const cantidad = String(item.tendencia).match(/\d+/)?.[0] ?? '0'
      return { ...item, tendencia: `${cantidad} pilot areas ready` }
    }

    if (tendenciaNormalizada.includes('EMPLEADOS CON MULTIPLES ROLES')) {
      const cantidad = String(item.tendencia).match(/\d+/)?.[0] ?? '0'
      return { ...item, tendencia: `${cantidad} employees with multiple roles` }
    }

    if (tendenciaNormalizada.includes('REGISTROS ACTIVOS DE SEGUIMIENTO')) {
      const cantidad = String(item.tendencia).match(/\d+/)?.[0] ?? '0'
      return { ...item, tendencia: `${cantidad} active tracking records` }
    }

    return item
  })

  panelTraducido.alertas = (panel.alertas ?? []).map((alerta) => {
    if (idioma === 'es') {
      return alerta
    }

    const coincidenciaRoles = String(alerta).match(/^(\d+)\s+empleados reportan multiples roles/i)
    if (coincidenciaRoles) {
      return `${coincidenciaRoles[1]} employees report multiple roles without formal time allocation.`
    }

    return traducirExacto(alerta, idioma)
  })

  return panelTraducido
}

